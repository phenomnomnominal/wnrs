const card = document.getElementById("card-content");

function changeLevel(level) {
  let questions = [];
  if (level === 1) {
    questions = [...LEVEL_ONE];
  }
  if (level === 2) {
    questions = [...LEVEL_TWO];
  }
  if (level === 3) {
    questions = [...LEVEL_THREE];
  }
  window.next = () => {
    const questionN = Math.floor(Math.random() * questions.length);
    const question = questions.splice(questionN, 1);
    card.innerText = question;
  };
  next();
}

changeLevel(1);
